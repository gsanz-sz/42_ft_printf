#include "libftprintf.h"

int	ft_isconversion(int c)
{
	return (c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i' ||
			c == 'u' || c == 'x' || c == 'X' || c == '%');
}

int	ft_isflag(int c)
{
	return (c == '-' || c == '0' || c == '.' || c == '*' || c == ' ');
}
